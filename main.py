#Função type para saber o tipo de uma variável
varX = 1_000_000.00
print(type(varX))

#Convertendo para string com str()
varX = str(varX)
print(type(varX))

#Convertendo para float com float()
varX = float(varX)
print(type(varX))

#Convertendo para inteiro com int()
varX = int(varX)
print(type(varX))

